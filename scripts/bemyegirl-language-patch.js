function renderLanguageSelector() {
  ensureLanguageSelectorStyles();
  const saved = localStorage.getItem("bemyegirl-language");
  if (saved === "it" || saved === "en") {
    applyQuizLanguage(saved);
    originalRenderQuestionForLanguage();
    return;
  }

  const actions = document.querySelector(".quiz-actions");
  const progress = document.getElementById("progressBar");
  if (actions) actions.style.display = "none";
  if (progress) progress.style.width = "0%";

  questionArea.innerHTML = `
    <div class="language-choice-box">
      <p class="quiz-kicker">Language / Lingua</p>
      <h2>Scegli la lingua</h2>
      <p>Puoi fare il quiz in italiano o in inglese.</p>
      <div class="language-choice-grid">
        <button type="button" data-quiz-language="it">Italiano</button>
        <button type="button" data-quiz-language="en">English</button>
      </div>
    </div>
  `;

  questionArea.querySelectorAll("[data-quiz-language]").forEach((button) => {
    button.addEventListener("click", () => {
      const language = button.getAttribute("data-quiz-language");
      if (actions) actions.style.display = "";
      applyQuizLanguage(language);
      originalRenderQuestionForLanguage();
    });
  });
}

