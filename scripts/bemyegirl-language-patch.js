let selectedQuizLanguage = null;
const originalRenderQuestionForLanguage = renderQuestion;
const originalRenderCategoryBreakdownForLanguage = renderCategoryBreakdown;
const originalGetRouteResultForLanguage = getRouteResult;

function applyTextPatchToQuestion(question, patch) {
  if (!question || !patch) return;
  if (patch.question) question.question = patch.question;
  if (patch.answers && question.answers) {
    patch.answers.forEach((label, index) => {
      if (question.answers[index]) question.answers[index].label = label;
    });
  }
  if (patch.ranges && question.ranges) {
    patch.ranges.forEach((label, index) => {
      if (question.ranges[index]) question.ranges[index].label = label;
    });
  }
  if (question.id === "red_flags") {
    if (patch.shortTitle) question.shortTitle = patch.shortTitle;
    if (patch.shortText) question.shortText = patch.shortText;
    if (patch.fullTitle) question.fullTitle = patch.fullTitle;
    if (patch.fullText) question.fullText = patch.fullText;
    if (patch.changeVersion) question.changeVersion = patch.changeVersion;
    [question.answers, question.fullAnswers, question.shortAnswers].forEach((list) => {
      if (!list) return;
      list.forEach((answer) => {
        const englishFull = getEmotionalBugAnswers().find((item) => item.value === answer.value && item.label === answer.label);
        const index = getEmotionalBugAnswers().findIndex((item) => item.label === answer.label);
        if (index >= 0 && patch.answers[index]) answer.label = patch.answers[index];
      });
    });
  }
}

function applyQuizLanguage(language) {
  selectedQuizLanguage = language;
  localStorage.setItem("bemyegirl-language", language);
  document.documentElement.lang = language === "it" ? "it" : "en";

  if (language !== "it") {
    renderQuestion = originalRenderQuestionForLanguage;
    return;
  }

  const dictionary = quizI18n.it;
  document.querySelector(".quiz-kicker") && (document.querySelector(".quiz-kicker").textContent = dictionary.ui.kicker);
  document.querySelector(".quiz-subtitle") && (document.querySelector(".quiz-subtitle").textContent = dictionary.ui.subtitle);
  const back = document.getElementById("backBtn");
  const next = document.getElementById("nextBtn");
  if (back) back.textContent = dictionary.ui.back;
  if (next) next.textContent = dictionary.ui.next;

  questions.forEach((question) => applyTextPatchToQuestion(question, dictionary.questions[question.id]));
  categoryDefinitions.forEach((category) => {
    if (dictionary.categories[category.id]) category.label = dictionary.categories[category.id];
  });

  getRouteResult = function(percentage) {
    const result = originalGetRouteResultForLanguage(percentage);
    const translated = dictionary.routes[result.route];
    return translated ? { ...result, ...translated } : result;
  };

  renderCategoryBreakdown = function(categories) {
    return originalRenderCategoryBreakdownForLanguage(categories)
      .replace("Category breakdown", dictionary.ui.categoryKicker)
      .replace("Strongest:", `${dictionary.ui.strongest}:`)
      .replace("Weakest:", `${dictionary.ui.weakest}:`);
  };

  renderQuestion = originalRenderQuestionForLanguage;
}

